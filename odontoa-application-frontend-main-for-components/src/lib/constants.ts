// Helper function to translate a single constant value
export const translateConstant = (
  key: string,
  category:
    | "toothParts"
    | "dgGroups"
    | "dgOptions"
    | "thGroups"
    | "thOptions"
    | "allTeeth",
  t: (key: string) => string
): string => {
  const translationKey = `constants.${category}.${key}`;
  const translated = t(translationKey);
  return translated !== translationKey ? translated : key;
};

