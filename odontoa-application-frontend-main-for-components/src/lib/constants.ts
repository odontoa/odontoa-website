/**
 * Determines if a tooth is a front tooth (incisors and canines) or side tooth (premolars and molars)
 * Front teeth: 11-13, 21-23, 31-33, 41-43 (FDI notation)
 * Side teeth: 14-18, 24-28, 34-38, 44-48 (FDI notation)
 * @param toothNumber - The tooth number in FDI notation (as number or string)
 * @returns true if front tooth, false if side tooth
 */
export const isFrontTooth = (toothNumber: number | string): boolean => {
  const num = typeof toothNumber === "string" ? parseInt(toothNumber, 10) : toothNumber;
  const lastDigit = num % 10;
  // Front teeth have last digit 1, 2, or 3
  return lastDigit >= 1 && lastDigit <= 3;
};

